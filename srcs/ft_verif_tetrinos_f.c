#include "libft.h"

int		ft_verif_tetrinos_f(char *s)
{
	int i;
	int	c;

	i = -1;
	c = 0;
	while (s[++i])
	{
		if (i != (5 * (c + 1) - 4 * ((c + 1) / 5) - 1))
		{
			if (s[i] != '.' && s[i] != '#')
				return (0);
		}
		else
		{
			if (s[i] != '\n')
				return (0);
			else
				c++;
		}
		if (i > 544)
			return (0);
	}
	if (i != ((c + 1) / 5) * 21 - 1)
		return (0);
	return ((c + 1) / 5);
}
